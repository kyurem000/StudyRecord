print("백문이 불여일견 \n백견이 불여일타") # \n을 넣으면 줄바꿈이 일어난다

print('저는 "나도코딩"입니다.')  # 작은 따옴표로 감싼다 

print("저는 \"나도코딩\"입니다.") # 역슬래시 큰따옴표는 그냥 따옴표를 출력해주는 용도로 쓴다 
print("저는 \'나도코딩\'입니다.") # 마찬가지로 작은따옴표도 출력 

# || :  문장 내에서 |
print("c:\\Users\\asd\\fghh\\zxcv") # 경로 출력 

# |r : 커서를 맨 앞으로 이동 
print("Red Apple\rPine") # |r을 이용해서 커서를 맨앞으로 이동시킨다 

# |b : 백스페이스 (한 글자 삭제)
print("Red\bApple")

# |t : 탭
print("Red\tApple")